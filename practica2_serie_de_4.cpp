/* 1.2 Diseñar un algoritmo que imprima y sume la serie denúmeros 4, 8, 12, 16,..., 400.
 *
 */

#include <iostream>

using namespace std;


int main(int argc, char** argv) {
	int numero;
	
	for(int i=1; numero < 400; i++) {
		numero = 4 * i;
        cout << numero << ", ";
    }
    
    cout << "\n\n";
	
	system("pause");
	return 0;
}

